const LEGACY_REQUIREMENT_ISLANDS = new Set(["Natural", "Fire", "Haven", "Oasis"]);
export const REQUIREMENT_MISMATCH_LOG = [];

function pushRequirementMismatch(issue)
{
  const exists = REQUIREMENT_MISMATCH_LOG.some((entry) =>
  {
    return (
      entry.systemKey === issue.systemKey &&
      entry.targetMonsterName === issue.targetMonsterName &&
      entry.requirementName === issue.requirementName &&
      entry.reason === issue.reason
    );
  });

  if (!exists)
  {
    REQUIREMENT_MISMATCH_LOG.push(issue);

    if (typeof import.meta !== "undefined" && import.meta.env?.DEV)
    {
      console.warn("Requirement validation issue", issue);
    }
  }
}

export function resolveRequirementIsland(monsterName, islandName)
{
  if (typeof islandName !== "string" || !islandName.trim())
  {
    return "";
  }

  return isRealBreedingIsland(islandName) ? islandName : "";
}

export function isLegacyRequirementIsland(islandName)
{
  return LEGACY_REQUIREMENT_ISLANDS.has(islandName);
}

export function getRequirementList(systemKey, targetMonsterName)
{
  return MONSTER_REQUIREMENTS[systemKey]?.[targetMonsterName] || [];
}

export function createSheetMonstersFromRequirements(systemKey, targetMonsterName)
{
  return getRequirementList(systemKey, targetMonsterName)
    .filter((item) =>
    {
      const isValidMonster = Boolean(MONSTER_DIRECTORY[item.name]);

      if (!isValidMonster)
      {
        pushRequirementMismatch({
          systemKey,
          targetMonsterName,
          requirementName: item.name,
          island: item.island,
          reason: "missing_monster_database_entry",
        });
      }

      return isValidMonster;
    })
    .map((item) => ({
      name: item.name,
      required: item.count,
      zapped: 0,
      breeding: 0,
      breedingAssignments: {},
      island: resolveRequirementIsland(item.name, item.island),
      requirementIsland: item.island,
    }));
}

